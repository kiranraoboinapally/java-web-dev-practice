class charB {
        public static void main(String[] args) {
       char input = 'k';
       if (input >= 'A' && input <= 'Z') {
          System.out.println("Input is a Capital Letter");
       } else if (input >= 'a' && input <= 'z') {
          System.out.println("Input is a Small Letter");
       } else {
          System.out.println("Special Symbol");
       }
 
    }
 }
 